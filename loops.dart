// Loops in dart.

void main(){
  print("Loops in Dart\n");


  // For loop demo:
  for (int i = 0; i<10; i++){

    print("this is looped");
  }
  

  // do-while demo:
  int no = 100;
  do{
    print("This is a do-while loop");
    print("No is $no");
  }while(no<50);


  // While loop demo:
  print("This is a while loop demo");
  int a = 0;
  while(a<10){
    print(a);
    a++;
  }
}